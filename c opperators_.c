#include<stdio.h>

int main()
{
    int a=12,b=3;
    int addition, subtraction, multiplication, division,modulus;
    addition=a+b;
    subtraction=a-b;
    multiplication=a*b;
    division=a/b;
    modulus=a*b;
    printf("addition of integers a,b is %d\n", addition);
    printf("subtraction of two integers a,b is %d\n",subtraction);
    printf("multiplication of two integers a,b is %d\n", multiplication);
    printf("division of two integers a,b is %d\n", division);
    printf("modulus of two integers a, b is %d\n",modulus);

    return 0;
}